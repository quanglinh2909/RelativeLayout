package com.quanglinh.layout.custom;

public class Size {
	private int with;
	private int height;
	public Size(int with, int height) {
		this.with = with;
		this.height = height;
	}
	/**
	 * @return the with
	 */
	public int getWith() {
		return with;
	}
	/**
	 * @param with the with to set
	 */
	public void setWith(int with) {
		this.with = with;
	}
	/**
	 * @return the height
	 */
	public int getHeight() {
		return height;
	}
	/**
	 * @param height the height to set
	 */
	public void setHeight(int height) {
		this.height = height;
	}
	

}
